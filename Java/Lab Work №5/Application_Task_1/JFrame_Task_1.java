    protected void getRoom(){
        if (currentInd >= 0) {
            textLengthRoom.setText(String.valueOf(roomList.get(currentInd).length));
            textWidthRoom.setText(String.valueOf(roomList.get(currentInd).width));
            textHeightRoom.setText(String.valueOf(roomList.get(currentInd).height));
        }
    }
    
    protected void setRoom(){
        if ((currentInd >= 0) && (textLengthRoom.getText() != null)) {
            roomList.get(currentInd).length = Double.parseDouble(textLengthRoom.getText());
            roomList.get(currentInd).width = Double.parseDouble(textWidthRoom.getText());

            try {
                roomList.get(currentInd).width = new Double (textWidthRoom.getText());
            } catch (NumberFormatException e) {
                roomList.get(currentInd).width = 0;
                textWidthRoom.setText(null);
            }

            try {
                roomList.get(currentInd).height = new Double(textHeightRoom.getText());
            } catch (NumberFormatException e) {
                roomList.get(currentInd).height = 0;
                textHeightRoom.setText(null);
            }
        }
    }
    
