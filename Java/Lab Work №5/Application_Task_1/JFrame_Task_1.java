    private void jButtonNewRoomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonNewRoomActionPerformed
        setRoom();
        roomList.add(new Room());
        currentInd = roomList.size()-1;
        getRoom();
        jTextFieldCount.setText(""+(currentInd+1));
        jSpinnerCrn.setValue(currentInd);
    }//GEN-LAST:event_jButtonNewRoomActionPerformed

    private void jButtonDelRoomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDelRoomActionPerformed
        roomList.remove(currentInd);
        currentInd = roomList.size()-1;
        if (currentInd == roomList.size()) {
            currentInd--;
            jSpinnerCrn.setValue(currentInd);
        } else {
            getRoom();
        }
        jTextFieldCount.setText("" + roomList.size());
    }//GEN-LAST:event_jButtonDelRoomActionPerformed

    private void jSpinnerCrnStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSpinnerCrnStateChanged
        setRoom();
        currentInd = (int) jSpinnerCrn.getValue();
        jTextFieldSquareRoom.setText(null);
        jTextFieldCalcSquareWithout.setText(null);
        if (currentInd >= roomList.size()) {
            currentInd = roomList.size()-1;
            jSpinnerCrn.setValue(currentInd);
        } else if (currentInd < 0) {
            currentInd = 0;
            jSpinnerCrn.setValue(currentInd);
        }
        getRoom();
    }//GEN-LAST:event_jSpinnerCrnStateChanged

    private void jButtonCalcSquareActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCalcSquareActionPerformed
        setRoom();
        for (Room room : roomList) {
            room.calcSquareWalls();
            DecimalFormat dF = new DecimalFormat("0.###");
            String value = dF.format(room.square);
            jTextFieldSquareRoom.setText(value);
        }
        getRoom();
    }//GEN-LAST:event_jButtonCalcSquareActionPerformed

    private void jButtonSquareRoomWithoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSquareRoomWithoutActionPerformed
        setRoom();
        for (Room room : roomList) {
            room.calcSquareWallsWithoutDoorAndWindow();
            DecimalFormat dF = new DecimalFormat("0.###");
            String value = dF.format(room.squareWithout);
            jTextFieldCalcSquareWithout.setText(value);
        }
        getRoom();
    }//GEN-LAST:event_jButtonSquareRoomWithoutActionPerformed

